public class OperadoresAritmeticos {

    void main() {

       // Soma
       System.out.println(10 + 7); 

       // Subtração
       System.out.println(8 - 3);

       // Multiplicação
       System.out.println(3 * 4);

       // Divisão
       System.out.println(10 / 2);

       // Resto da divisão (MOD)
       System.out.println(7 % 3);

    }
    
}
