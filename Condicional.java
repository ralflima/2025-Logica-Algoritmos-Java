public class Condicional {
 
    void main() {

        // Variável
        double nota = 6;

        // Condicional
        if(nota >= 7){
            System.out.println("Aprovado(a)");
        }else if(nota >= 5){
            System.out.println("Em recuperação");
        }else{
            System.out.println("Reprovado(a)");
        }

    }
    
}